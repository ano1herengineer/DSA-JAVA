import java.io.*;

public class playwithswitch
{
    public static void main(String args[])
    {
        int number =3;

        switch(number)
        {
            case 1:
            System.out.println("Samosa");
            break;

            case 2:
            System.out.println("Burger");
            break;

            case 3:
            System.out.println("Milkshake");
            break;

            default:
            System.out.println("VAMOS");
        }
    }
}